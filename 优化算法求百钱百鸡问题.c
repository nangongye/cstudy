//百钱百鸡问题算法优化

#include<stdio.h>


	int main()
	{
		int x,y,z;	//定义变量xyz,x为公鸡，y为母鸡，z为小鸡 
		
		for(x=0; x<=12; x+=4)	//由穷举法可得公鸡的最大值为 12，且自增 4都有一个解，在此基础上，可用x<=12做判断，每次循环结束自增 4 
			{
				y = 25 - (x/4)*7;// x+y+z=100  	5x+3y+1/3*z=100 两个方程化简即可 
				z = 100 - x - y;
				printf("公鸡：%d, 母鸡:%d, 小鸡:%d\n",x,y,z);
			}
		
		return 0;
	} 
